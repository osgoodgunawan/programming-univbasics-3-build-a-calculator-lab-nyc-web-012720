def addition(num1, num2)
  sum=num1+num2
  puts sum
  sum
end

def subtraction(num1, num2)
  minus=num1-num2
  puts minus
  return minus
end

def division(num1, num2)
  divide=num1/num2
  puts divide
  divide
end

def multiplication(num1, num2)
  multiply=num1*num2
  puts multiply
  multiply
end

def modulo(num1, num2)
  remainder=num1%num2
  puts remainder
  remainder
end

def square_root(num)
  square=num**num
  puts square
  square
end